def build_context_prompt(system_prompt: str, past_context: str, user_message: str) -> str:
    """აწყობს საბოლოო prompt-ს: სისტემური ინსტრუქცია + წინა კონტექსტი + ახალი შეტყობინება."""
    context_block = f"\n\nწინა საუბრის კონტექსტი:\n{past_context}\n" if past_context else ""
    return f"{system_prompt}{context_block}\n\nმომხმარებლის კითხვა: {user_message}"
